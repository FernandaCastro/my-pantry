CREATE SCHEMA IF NOT EXISTS AUTHORIZATION pantry;
CREATE TABLE IF NOT EXISTS pantry.PRODUCT(
    ID SERIAL PRIMARY KEY,
    CODE VARCHAR(20) NOT NULL UNIQUE,
    DESCRIPTION VARCHAR(30) NOT NULL,
    UNIT VARCHAR(10),
    AMOUNT VARCHAR(10)
);
CREATE TABLE IF NOT EXISTS pantry.PANTRY(
    ID SERIAL PRIMARY KEY,
    NAME VARCHAR(30) NOT NULL UNIQUE,
    TYPE CHAR(1),
    ACTIVE BOOLEAN
);
CREATE TABLE IF NOT EXISTS pantry.PANTRY_PRODUCT(
    PANTRY_ID INTEGER NOT NULL REFERENCES PANTRY (ID),
    PRODUCT_ID INTEGER NOT NULL REFERENCES PRODUCT (ID),
    IDEAL_QUANTITY INTEGER,
    CURRENT_QUANTITY INTEGER,

    CONSTRAINT PANTRY_PRODUCT_UNIQUE UNIQUE ( PANTRY_ID, PRODUCT_ID )
);
